x = int (input("Masukkan angka: ")) # Meminta input angka

if x < 5: # Jika angka kurang dari 5
    print(x, "Kurang dari 5") # Print kurang dari 5
elif x == 5: # Jika angka sama dengan 5
    print(x, "Sama dengan 5") # Print sama dengan 5
else: # Jika angka lebih dari 5
    print(x, "Lebih dari 5") # Print lebih dari 5